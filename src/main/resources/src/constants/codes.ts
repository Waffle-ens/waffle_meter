export const GROUPED_BY_JOB: GroupedJobSkills[] = Object.keys(JOB_PREFIX_MAP).map((job) => {
  const jobSkills = SKILLS.filter((skill) => skill.job === job);
  return {
    job,
    normalSkills: jobSkills.filter((skill) => !skill.isStigma).map((skill) => skill.code),
    stigmaSkills: jobSkills.filter((skill) => skill.isStigma).map((skill) => skill.code),
  };
});

